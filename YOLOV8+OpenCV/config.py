# -*- coding: utf-8 -*-
import os


class Config:
    """
    集中管理所有配置参数。
    好处：
    1. 修改一处，全局生效，避免硬编码散落在各文件里
    2. 面试时能体现"工程化意识"
    3. 环境变量 + 默认值的写法，方便部署时覆盖
    """

    # ==================== 云端模型配置 ====================
    # 阿里云百炼 API Key
    # 建议通过环境变量注入，不要把密钥写死在代码里提交到 GitHub
    QWEN_API_KEY = os.getenv("QWEN_API_KEY", "")

    # 百炼兼容 OpenAI 的接口地址
    # 为什么用兼容模式？因为这样可以复用 OpenAI 风格的调用代码，
    # 以后如果换 GPT-4V、Gemini 等，改动很小
    QWEN_BASE_URL = "https://dashscope.aliyuncs.com/compatible-mode/v1"

    # 视觉语言模型选择
    # qwen-vl-plus: 性价比高，推荐先用这个跑通
    # qwen-vl-max: 能力更强但更贵，可用于对比实验
    QWEN_MODEL = "qwen-vl-max"

    # ==================== 边缘端模型配置 ====================
    # YOLOv8n 是最小的版本，n 代表 nano
    # 为什么选 nano？因为项目强调"端侧轻量化"和"实时性"
    YOLO_MODEL = "yolov8m.pt"

    # 运行设备，"0" 表示第一张 GPU，"cpu" 表示用 CPU
    DEVICE = 0

    # ==================== 视频处理配置 ====================
    # 抽帧频率：每秒抽多少帧
    # 为什么设 1 而不是 30？因为 VLM 是按次收费的，抽帧越频繁越贵
    # 驾驶场景变化相对慢，1fps 通常够做风险分析
    FPS = 0.5

    # 是否保存原始帧图片（未标注的）
    # False 可以节省一半磁盘空间，适合批量处理场景
    # True 适合需要回溯查看原图的场景
    SAVE_RAW_FRAMES = False

    # ==================== VLM 触发规则配置 ====================
    # 不同类别的触发置信度阈值
    # 行人最重要，所以阈值最低（0.5 就触发）
    # 车辆需要更确定才触发
    # 交通灯/标志本身小，阈值也低
    TRIGGER_CLASSES = {
        'person': 0.5,
        'car': 0.4,
        'truck': 0.4,
        'bus': 0.4,
        'motorcycle': 0.4,
        'bicycle': 0.4,
        'traffic light': 0.3,
        'stop sign': 0.3,
    }

    # 画面区域划分
    # x 方向：0.3~0.7 认为是画面中心，也就是本车正前方
    # y 方向：0.5~1.0 认为是画面下半部分，也就是距离较近的物体
    # 这种归一化坐标的好处是和视频分辨率无关
    CENTER_REGION = (0.3, 0.7)
    NEAR_REGION = (0.5, 1.0)

    # ==================== 异步控制配置 ====================
    # 最大并发调用 VLM 的数量
    # 为什么不能无限大？
    # 1. 本地内存有限，同时读太多图会爆
    # 2. 远端 API 可能限流
    # 3. 并发太高反而因为网络拥堵导致平均延迟上升
    MAX_CONCURRENT_API = 8
